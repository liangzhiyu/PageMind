let _currentLang = 'en';

// Get a translated string
function t(key) {
  return TRANSLATIONS[_currentLang]?.[key] ?? TRANSLATIONS.en[key] ?? key;
}

// Apply translations to the current page DOM
function applyI18n(lang) {
  _currentLang = TRANSLATIONS[lang] ? lang : 'en';
  document.documentElement.lang = _currentLang;

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const val = t(el.dataset.i18n);
    if (val) el.textContent = val;
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const val = t(el.dataset.i18nPlaceholder);
    if (val) el.placeholder = val;
  });
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    const val = t(el.dataset.i18nTitle);
    if (val) el.title = val;
  });

  // Update active state on language buttons (settings page)
  document.querySelectorAll('.btn-lang').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === _currentLang);
  });
}
